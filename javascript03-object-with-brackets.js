// getting a js object with bracket notation
// - for if object property has space in it's name

var testObj = {
  "an entree": "hamburger",
  "my side": "veggies",
  "the drink": "water"
};


var entreeValue = testObj["an entree"];
var drinkValue = testObj["the drink"];




